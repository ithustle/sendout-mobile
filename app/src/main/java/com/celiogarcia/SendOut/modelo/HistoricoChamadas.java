public class HistoricoChamadas {
    private String numero;
    private String dataChamada;
    private String tempoDeChamada;

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public String getDataChamada() {
        return dataChamada;
    }

    public void setDataChamada(String dataChamada) {
        this.dataChamada = dataChamada;
    }

    public String getTempoDeChamada() {
        return tempoDeChamada;
    }

    public void setTempoDeChamada(String tempoDeChamada) {
        this.tempoDeChamada = tempoDeChamada;
    }
}
